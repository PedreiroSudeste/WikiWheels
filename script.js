function avaliar1(){
    if (document.querySelector(".btnAvaliar1").style.backgroundColor == "") {
        document.querySelector('.btnAvaliar1').style.backgroundColor = "rgb(212 175 55)";
        document.querySelector('.btnAvaliar1').style.color = "black";
    }else{
        document.querySelector('.btnAvaliar1').style.backgroundColor = "";
        document.querySelector('.btnAvaliar1').style.color = "white";
    }
}
